/**
 * <i>FliegelvanFlandernJulian</i> demonstrates Gregorian To-From Julian Date Conversion Functionality. The
 * References are:
 * 
 * <br><br>
 *  <ul>
 *  	<li>
 * 			Fenton, D. (2001): Julian to Calendar Date Conversion
 * 				http://mathforum.org/library/drmath/view/51907.html
 *  	</li>
 *  	<li>
 * 			Fliegel, H. F., and T. C. van Flandern (1968): A Machine Algorithm for Processing Calendar Dates
 * 				<i>Communications of the ACM</i> <b>11</b> 657
 *  	</li>
 *  </ul>
 *  
 * <br><br>
 *  <ul>
 *		<li><b>Module </b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/AnalyticsCore.md">Analytics Core Module</a></li>
 *		<li><b>Library</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/FixedIncomeAnalyticsLibrary.md">Fixed Income Analytics Library</a></li>
 *		<li><b>Project</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/sample/README.md">Sample</a></li>
 *		<li><b>Package</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/sample/date/README.md">Date Management API</a></li>
 *  </ul>
 * <br><br>
 * 
 * @author Lakshmi Krishnamurthy
 */

public class FliegelvanFlandernJulian {

	public static final void main (
		final String[] astrArgs)
		throws Exception
	{
		EnvManager.InitEnv ("");

		int iYear = 2001;
		int iMonth = 2;
		int iDate = 24;

		int iJulian = DateUtil.ToJulian (
			iYear,
			iMonth,
			iDate
		);

		System.out.println (
			"\tGregorian: [" + iYear + "/" +
			FormatUtil.FormatDouble (iMonth, 2, 0, 1., false) + "/" +
			FormatUtil.FormatDouble (iDate, 2, 0, 1., false) +
			"]; Julian: " + iJulian
		);

		System.out.println ("\tJulian: " + iJulian + "; Gregorian: [" + DateUtil.YYYYMMDD (iJulian) + "]");

		EnvManager.TerminateEnv();
	}
}
