public class DIFutures {
	public static final void main (
		final String[] astrArgs)
		throws Exception
	{
		/*
		 * Initialize the Credit Analytics Library
		 */

		EnvManager.InitEnv ("");

		String strCurrency = "BRL";

		JulianDate dtToday = DateUtil.Today().addBusDays (
			0,
			strCurrency
		);

		MergedDiscountForwardCurve dcOIS = OvernightIndexCurve.MakeDC (
			dtToday,
			strCurrency
		);

		SingleStreamComponent diFutures = SingleStreamComponentBuilder.Deposit (
			dtToday,
			dtToday.addTenorAndAdjust (
				"1M",
				strCurrency
			),
			OvernightLabel.Create (
				strCurrency
			)
		);

		CurveSurfaceQuoteContainer mktParams = MarketParamsBuilder.Create (
			dcOIS,
			null,
			null,
			null,
			null,
			null,
			null,
			null
		);

		ValuationParams valParams = new ValuationParams (
			dtToday,
			dtToday,
			strCurrency
		);

		Map<String, Double> mapDIFuturesOutput = diFutures.value (
			valParams,
			null,
			mktParams,
			null
		);

		for (Map.Entry<String, Double> me : mapDIFuturesOutput.entrySet())
			System.out.println ("\t" + me.getKey() + " => " + me.getValue());

		EnvManager.TerminateEnv();
	}
}
