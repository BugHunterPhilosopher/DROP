/**
 * <i>TenorQuote</i> holds the Instrument Tenor and Closing Quote.
 *
 *	<br><br>
 *  <ul>
 *		<li><b>Module </b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/NumericalCore.md">Numerical Core Module</a></li>
 *		<li><b>Library</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/AlgorithmSupportLibrary.md">Algorithm Support</a></li>
 *		<li><b>Project</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/feed/README.md">Feed</a></li>
 *		<li><b>Package</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/feed/loader/README.md">Loader</a></li>
 *  </ul>
 * 
 * @author Lakshmi Krishnamurthy
 */

public class TenorQuote {
	private java.lang.String _strTenor = "";
	private double _dblQuote = java.lang.Double.NaN;

	/**
	 * TenorQuote Constructor
	 * 
	 * @param strTenor The Closing Tenor
	 * @param dblQuote The Closing Quote
	 * 
	 * @throws java.lang.Exception Thrown if the Inputs are Invalid
	 */

	public TenorQuote (
		final java.lang.String strTenor,
		final double dblQuote)
		throws java.lang.Exception
	{
		if (null == (_strTenor = strTenor) || _strTenor.isEmpty() ||
			!org.drip.numerical.common.NumberUtil.IsValid (_dblQuote = dblQuote))
			throw new java.lang.Exception ("TenorQuote Constructor => Invalid Inputs");
	}

	/**
	 * Retrieve the Closing Tenor
	 * 
	 * @return The Closing Tenor
	 */

	public java.lang.String tenor()
	{
		return _strTenor;
	}

	/**
	 * Retrieve the Closing Quote
	 * 
	 * @return The Closing Quote
	 */

	public double quote()
	{
		return _dblQuote;
	}

	@Override public java.lang.String toString()
	{
		return _strTenor + "=" + _dblQuote;
	}
}
