/**
 * <i>Fixed</i> contains the fixed holiday’s date and month. Holidays are generated on a per-year basis by
 * applying the year, and by adjusting the date generated.
 *
 *	<br><br>
 *  <ul>
 *		<li><b>Module </b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/AnalyticsCore.md">Analytics Core Module</a></li>
 *		<li><b>Library</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/FixedIncomeAnalyticsLibrary.md">Fixed Income Analytics</a></li>
 *		<li><b>Project</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/analytics/README.md">Analytics</a></li>
 *		<li><b>Package</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/analytics/eventday/README.md">Event Day</a></li>
 *  </ul>
 *
 * @author Lakshmi Krishnamurthy
 */

public class Fixed extends Base {
	private int _iDay = 0;
	private int _iMonth = 0;
	private Weekend _wkend = null;

	/**
	 * Construct the object from the day, month, weekend, and description
	 * 
	 * @param iDay Day
	 * @param iMonth Month
	 * @param wkend Weekend Object
	 * @param strDescription Description
	 */

	public Fixed (
		final int iDay,
		final int iMonth,
		final Weekend wkend,
		final java.lang.String strDescription)
	{
		super (strDescription);

		_iDay = iDay;
		_wkend = wkend;
		_iMonth = iMonth;
	}

	@Override public int dateInYear (
		final int iYear,
		final boolean bAdjust)
	{
		int iDate = org.drip.analytics.date.DateUtil.CreateFromYMD (iYear, _iMonth, _iDay).julian();

		try {
			if (bAdjust) return Base.rollHoliday (iDate, true, _wkend);
		} catch (java.lang.Exception e) {
			e.printStackTrace();
		}

		return iDate;
	}
}
