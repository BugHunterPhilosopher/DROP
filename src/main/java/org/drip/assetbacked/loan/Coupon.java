/**
 * <i>Coupon</i> contains the current Loan Annualized Coupon Rate and Frequency.
 *
 *	<br><br>
 *  <ul>
 *		<li><b>Module </b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/AnalyticsCore.md">Analytics Core Module</a></li>
 *		<li><b>Library</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/AssetBackedAnalyticsLibrary.md">Asset Backed Analytics</a></li>
 *		<li><b>Project</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/assetbacked">Asset Backed</a></li>
 *		<li><b>Package</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/assetbacked/loan">Loan</a></li>
 *  </ul>
 *
 * @author Lakshmi Krishnamurthy
 */

public class Coupon {
	private int _iFrequency = -1;
	private double _dblRate = java.lang.Double.NaN;

	/**
	 * Coupon Constructor
	 * 
	 * @param dblRate Loan Coupon Rate
	 * @param iFrequency Loan Coupon Frequency
	 * 
	 * @throws java.lang.Exception Thrown if the Inputs are Invalid
	 */

	public Coupon (
		final double dblRate,
		final int iFrequency)
		throws java.lang.Exception
	{
		if (!org.drip.quant.common.NumberUtil.IsValid (_dblRate = dblRate) || 0 >= (_iFrequency =
			iFrequency))
			throw new java.lang.Exception ("Coupon Constructor => Invalid Inputs");
	}

	/**
	 * Retrieve the Loan Coupon Frequency
	 * 
	 * @return The Loan Coupon Frequency
	 */

	public int frequency()
	{
		return _iFrequency;
	}

	/**
	 * Retrieve the Loan Coupon Rate
	 * 
	 * @return The Loan Coupon Rate
	 */

	public double rate()
	{
		return _dblRate;
	}
}
