/**
 * <i>BernsteinPolynomial</i> provides the evaluation of the BernsteinPolynomial and its derivatives for a
 * specified variate.
 * <br>
 *  <ul>
 *		<li><b>Module</b>        = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/function">Function</a></li>
 *		<li><b>Package</b>       = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/function/r1tor1">R<sup>1</sup> To R<sup>1</sup></a></li>
 *		<li><b>Specification</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/Docs/Internal/MachineLearning">Machine Learning Library</a></li>
 *  </ul>
 *
 * @author Lakshmi Krishnamurthy
 */

public class BernsteinPolynomial extends org.drip.function.r1tor1.UnivariateConvolution {

	/**
	 * Construct a BernsteinPolynomial instance
	 * 
	 * @param iBaseExponent Base Exponent
	 * @param iComplementExponent Complement Exponent
	 * 
	 * @throws java.lang.Exception Thrown if the inputs are invalid
	 */

	public BernsteinPolynomial (
		final int iBaseExponent,
		final int iComplementExponent)
		throws java.lang.Exception
	{
		super (new org.drip.function.r1tor1.NaturalLogSeriesElement (iBaseExponent), new
			org.drip.function.r1tor1.UnivariateReflection (new org.drip.function.r1tor1.NaturalLogSeriesElement
				(iComplementExponent)));
	}

	public static final void main (
		final java.lang.String[] astrArgs)
		throws java.lang.Exception
	{
		BernsteinPolynomial bp = new BernsteinPolynomial (3, 3);

		System.out.println ("BPDeriv[0.25] = " + bp.derivative (0.25, 1));
	}
}
